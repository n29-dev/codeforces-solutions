ll bs(ll packs, ll sum, int x)
{
    ll l = 0, r = 1e9, a = 0, m = 0;

    while (l <= r)
    {
        m = (l + r) / 2;

        if ((packs * m) + sum <= x)
            a = m, l = m + 1;
        else
            r = m - 1;
    }
    return a;
}

void solve()
{
    int n, x;
    cin >> n >> x;
    vector<int> v(n + 1);
    v[0] = -1;

    ll sum = 0, packs = 0, ans = 0, d = 0, days = 0;

    for (int i = 0; i < n; i++)
        cin >> v[i];

    sort(v.begin(), v.end());

    for (int i = 1; i <= n; i++)
    {
        if (sum + v[i] <= x)
            sum += v[i], packs++, ans++;
        else
            break;
    }

    while (packs)
    {
        d = bs(packs, sum + (days * packs), x);
        ans += d * packs;
        days += d;
        sum -= v[packs];
        packs--;
    }

    cout << (ans) << nline;
}
