void solve()
{
    int n, c, d;
    cin >> n >> c >> d;

    vector<int> v(n);
    vector<ll> cost(n);

    for (int i = 0; i < n; i++)
        cin >> v[i];

    sort(v.begin(), v.end());

    ll duplicates = 0, prevSize = v.size();

    v.resize(distance(v.begin(), unique(v.begin(), v.end())));

    n = v.size();
    duplicates = prevSize - n;

    ll costSum = 0;
    ll curr = 1;

    for (int i = 0; i < n; i++)
    {
        if (v[i] != curr)
        {
            costSum += (v[i] - curr) * d;
        }

        curr = v[i] + 1;
        cost[i] = costSum;
    }

    ll ans = LLONG_MAX, tmp = 0;

    for (int i = n - 1; i >= 0; i--)
    {
        tmp = (n - 1) - i;
        ans = min(ans, cost[i] + (tmp * c));
    }

    tmp = n;
    ans = min(ans, (tmp * c) + d);

    ans += duplicates * c;
    cout << ans << nline;
}

