void solve()
{
  int n, k;
  cin >> n >> k;
  vector<vector<int>> a(n);
  vector<vector<int>> b(n);
  int y;

  for (int i = 0; i < n; i++)
  {
    for (int j = 0; j < n; j++)
    {
      cin >> y;
      a[i].push_back(y);
      b[i].push_back(0);
    }
  }

  int c = 0;
  int z = (n * n) / 2;

  for (int i = 0; i < n && z; i++)
  {
    for (int j = 0; j < n && z; j++, z--)
    {
      if (a[i][j] != a[n - (1 + i)][n - (1 + j)])
      {
        c++;
      }
    }
  }

  if (c > k)
  {
    cout << "NO" << nline;
  }
  else if (n % 2 == 1)
  {
    cout << "YES" << nline;
  }
  else if ((k - c) % 2 == 0)
  {
    cout << "YES" << nline;
  }
  else
  {
    cout << "NO" << nline;
  }

  // debug(b);
}

